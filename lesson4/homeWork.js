// 6 . Сделайте функцию, которая параметрами принимает 2 числа. Если их сумма больше 10 - пусть вернет true, а если нет то - false.

function boolSum(a, b) {
  return a + b > 10 ? true : false
}

console.log(boolSum(6, 2))

